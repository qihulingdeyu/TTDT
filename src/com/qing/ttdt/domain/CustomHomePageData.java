package com.qing.ttdt.domain;


public class CustomHomePageData {

    private int icon;
    private String txt;
    private String num;
    private boolean chbox;
    
    public int getIcon() {
        return icon;
    }
    public void setIcon(int icon) {
        this.icon = icon;
    }
    public String getTxt() {
        return txt;
    }
    public void setTxt(String txt) {
        this.txt = txt;
    }
    public String getNum() {
        return num;
    }
    public void setNum(String num) {
        this.num = num;
    }
    public boolean isChbox() {
        return chbox;
    }
    public void setChbox(boolean chbox) {
        this.chbox = chbox;
    }
    
}
